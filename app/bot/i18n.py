def t(lang: str, key: str, **kwargs: Any) -> str:
    lang = lang if lang in _STRINGS else "ru"
    text = _STRINGS[lang].get(key) or _STRINGS["en"].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text


